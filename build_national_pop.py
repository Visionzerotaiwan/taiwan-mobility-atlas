COUNTY_RENAME = {
    '桃園縣':'桃園市',
}
# Modern 直轄市 → all sub-divisions are 區
MUNICIPAL = {'新北市', '台中市', '台南市', '高雄市', '桃園市'}
TOWN_RENAME = {
    ('彰化縣','員林鎮'): '員林市',
    ('苗栗縣','頭份鎮'): '頭份市',
}

def norm(s: str) -> str:
    return s.replace('臺','台').strip().rstrip('　').strip()

def normalize_town(county, town):
    if county in MUNICIPAL and town.endswith(('鄉','鎮','市')) and not town.endswith('區'):
        town = town[:-1] + '區'
    return TOWN_RENAME.get((county, town), town)

result = defaultdict(dict)
for line in RAW_ROWS.strip().split('\n'):
    site, pop = line.rsplit(',', 1)
    site = norm(site)
    pop = int(pop)
    # Extract county + district
    m = re.match(r'^([\w]+?[縣市])(.+)$', site)
    if not m: continue
    cty = COUNTY_RENAME.get(m.group(1), m.group(1))
    twn = normalize_town(cty, m.group(2))
    result[cty][twn] = pop

# Stats
total_districts = sum(len(v) for v in result.values())
total_pop = sum(sum(v.values()) for v in result.values())
print(f"Counties: {len(result)} | Districts: {total_districts} | Population: {total_pop:,}")

